				<p>ひばり音楽教室は、表面的な技術向上は目指していません。音楽の素晴らしさを感じる心を育てることを第一に考え、それを表現するために必要なことを一人ひとりに合わせて考えます。<br>昨今、大小さまざまな音楽コンクールが全国各地で行われていますが、そこに集う小さな音楽家のうち、「音楽が好きで好きで仕方がない」と思っている子はどれほどいるでしょうか。出番直前の控え室を見ていると、親や先生に無理矢理練習させられ、怒鳴られながらステージに向かう子が少なくありません。技術だけを求め、心を無くしたかのような演奏をする人の多さに驚かされます。スポーツの世界では「心技体」の精神の重要性が認知されています。しかし、同じパフォーマーであるはずの音楽家は、なぜ心を失わせてまで技だけをもとめるのでしょう。<br>
私はピティナ・ピアノコンペティション特級のグランプリを受賞しています。私がコンクールで演奏したプログラムはすべて、私が心から好きだと思い、心の底から表現したいと思う曲で揃えました。例えばバッハは中学生部門の課題曲で出るような曲ですし、ベートーヴェンは高校生部門の課題曲でもありました。コンクール映えのしない、いわば「勝つため」のプログラムではない選曲でありながらグランプリを受賞できたのは、私自身が心の重要性を認識していたからでもあります。<br>
当たり前の事ですが、バッハもベートーヴェンも人であり、心を持っています。彼らの作品が色褪せず、今も演奏され続けているのは、そこに内包されている精神の素晴らしさ、情感の豊かさによるものでしょう。自分の技巧を披露するためではなく、作品の素晴らしさを享受できる喜びを感じ、それを人々と共有するために演奏するほうが幸せだと思うのです。<br>
音を追うのではなく心を追う、その意志こそが自分自身の音楽を生み出した、さらには音楽を超えた自己理解、他者理解の要となると信じています。</p>
